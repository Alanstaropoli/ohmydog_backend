package entities;

public class Perro {
    private String nombre;
    private String raza;
    private String sexo;
    private Integer edad;

    public Perro(String nombre, String raza, String sexo, Integer edad) {
        this.nombre = nombre;
        this.raza = raza;
        this.sexo = sexo;
        this.edad = edad;
    }
}
